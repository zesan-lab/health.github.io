// Get the navigation element
var navBar = document.getElementById('main-nav');

// Add event listeners for mouseover and mouseout
navBar.addEventListener('mouseover', function () {
    // Change the background color on mouseover
    navBar.style.backgroundColor = '#05794c'; // Change this to match your hover color
});

navBar.addEventListener('mouseout', function () {
    // Change the background color back on mouseout
    navBar.style.backgroundColor = '#05794c'; // Change this to your default color
});


  